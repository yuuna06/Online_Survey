<?php
/**
 * signout.php
 * サインアウト処理
 * 概要：サーバ上のメモリ領域（セッション）とクライアントのブラウザ（クッキー）の両方から、
 * ユーザーの認証情報を完全に抹消し、トップページ（index.php）へリダイレクトする 。
 */

// 1. 共通モジュールファイルの読み込み
// 仕様書に基づき、共通のセッション破棄関数が定義されている「auth.php」を読み込む。
require_once 'auth.php'; [cite: 66]

// 2. セッション破棄処理の実行
// auth.php（またはsecurity.php）のセッション破棄関数を呼び出す。
// 
// 【セッションは破棄関数の処理内容 】
// ・メモリ上のセッション変数（$_SESSION）を空にする（ $_SESSION = array(); ）
// ・ブラウザ側のセッションIDのCookieを削除する
// ・session_destroy() を実行し、サーバ側のセッション実データを完全に破棄する
// 
// ※関数名は班で決めた実際の共通関数名（例: destroy_session() など）に書き換える。
destroy_session_common(); 

// 3. 画面遷移（リダイレクト）
// セッションの無効化処理が完了した後、トップページへ再読み込み（リダイレクト）させます 。
header('Location: index.php'); [cite: 66]

// 後続のスクリプトが誤って実行されないよう、必ず処理を完全に終了。
exit; [cite: 66]
